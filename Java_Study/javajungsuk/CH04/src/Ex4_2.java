public class Ex4_2 {
    public static void main(String[] args) {
        int[] arr = new int[10]; // 길이가 5인 int배열 arr을 생성
        // 배열의 길이는 배열명.length
        System.out.println("arr.length = " + arr.length);

        for (int i = 0; i < arr.length; i++) {
            System.out.println("arr[" + i + "] = " + arr[i]);
        } // end for
    }// end main
}// end class
