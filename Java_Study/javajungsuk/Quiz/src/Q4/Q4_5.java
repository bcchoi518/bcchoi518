package Q4;

public class Q4_5 {
  public static void main(String[] args) {
    int i = 0;
    while (i <= 10) {
      int j = 0;
      while (j <= i) {
        System.out.print("*");
        j++;
      } // end while
      System.out.println();
      i++;
    } // end while
  }// end main
}// end Q4_5
